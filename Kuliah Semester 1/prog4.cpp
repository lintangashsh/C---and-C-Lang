#include <iostream>
using namespace std;

// operasi penambahan dan pengurangan
int main()
{
    int a, b;
    a = 10;
    b = 5;

    cout << "Nilai a = " << a << endl;
    cout << "Nilai ++a = " << ++a << endl;
    cout << "Nilai a = " << a << endl;
    cout << "Nilai a++ = " << a++ << endl;
    cout << "Nilai a = " << a << endl;
    cout << "Nilai b = " << b << endl;
    cout << "Nilai --b = " << --b << endl;
    cout << "Nilai b = " << b << endl;
    cout << "Nilai b-- = " << b-- << endl;
    cout << "Nilai b = " << b << endl;

    system("pause");
    return 0;
}